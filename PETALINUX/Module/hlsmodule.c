static ssize_t dev_read(struct file *filep,char *buf,size_t len,loff_t *off) {
   if(resp_ready) {
      resp_ready=0;
      // print len - kkk
      printk(KERN_ALERT "reading device rx: %d\n",(int)len);
      int n = sprintf(ker_buf,"%d\n",result);
      copy_to_user(buf,ker_buf,n);
      printk(KERN_ALERT "returning %s rx: %d\n",ker_buf,n);
      printk(KERN_ALERT "len inside read is %d\n",len);
      return n;
   } else {
      printk(KERN_ALERT "resp_ready is %d \n", resp_ready);
   }
   return 0;
}

