static int hlsmodule_remove(struct platform_device *pdev)
{
	struct device *dev = &pdev->dev;
	struct hlsmodule_local *lp = dev_get_drvdata(dev);
	free_irq(lp->irq, lp);
	release_mem_region(lp->mem_start, lp->mem_end - lp->mem_start + 1);
	kfree(lp);
	dev_set_drvdata(dev, NULL);
	return 0;
}

